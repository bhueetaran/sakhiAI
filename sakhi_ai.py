# ── Helper functions ──────────────────────────────────────

def get_red_flag_reason(symptom, lang):
    symptom_clean = symptom.lower().strip()
    flags = RED_FLAGS_TRANSLATED.get(symptom_clean, {})
    return flags.get(lang, flags.get("English", ""))

def get_maternal_warning(symptom, lang):
    symptom_clean = symptom.lower().strip()
    warnings = MATERNAL_WARNINGS_TRANSLATED.get(symptom_clean, {})
    return warnings.get(lang, warnings.get("English", ""))

def get_screenings(lang):
    return SCREENINGS_TRANSLATED.get(lang, SCREENINGS_TRANSLATED["English"])

def detect_red_flags(selected_symptoms, lang):
    red_flags     = []
    for symptom in selected_symptoms:
        reason = get_red_flag_reason(symptom, lang)
        if reason:
            red_flags.append((symptom, reason))
    return red_flags

def detect_maternal_alerts(selected_symptoms, is_pregnant, lang):
    if not is_pregnant:
        return []
    alerts = []
    for symptom in selected_symptoms:
        warning = get_maternal_warning(symptom, lang)
        if warning:
            alerts.append((symptom, warning))
    return alerts

