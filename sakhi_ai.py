    # ── STEP 7: LLM Recommendations (Flan-T5) ────────────
    print("\n⏳ Step 7: Generating recommendations with Flan-T5...")
    top_disease = matched_diseases[0][0] if matched_diseases else "unknown condition"
    precautions = get_precautions(top_disease)

    flan_prompt = f"""
    A woman has these symptoms: {', '.join(extracted)}.
    Severity is {urgency}. {'She is pregnant.' if is_pregnant else ''}
    Possible condition: {top_disease}.
    What are 3 simple health recommendations for her?
    """
