def screen_pcos_risk(user_symptoms):
    """
    Flags potential PCOS based on symptom patterns.
    """
    pcos_symptoms = [
        "irregular periods", "missed period",
        "unusual bleeding", "unusual discharge",
        "pain during intercourse", "weight gain",
        "fatigue or tiredness", "severe pelvic pain"
    ]
    matches    = [s for s in user_symptoms if s in pcos_symptoms]
    risk_score = len(matches) / len(pcos_symptoms)
    return matches, round(risk_score * 100, 1)


