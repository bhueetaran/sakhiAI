def run_sakhai_pipeline(user_input, language, is_pregnant,
                         age=25, systolic_bp=120, diastolic_bp=80,
                         blood_glucose=10, body_temp=98.6, heart_rate=80):
    """
    Full SakhiAI pipeline:
    1. NLP extracts symptoms from free text
    2. Dataset engine matches diseases + severity
    3. Maternal risk predicted from real data
    4. Flan-T5 generates personalized recommendations
    5. Report translated to chosen language
    """

    print("="*55)
    print(" SakhiAI — Running Full AI Pipeline")
    print("="*55)

    # ── STEP 1: NLP Symptom Extraction ───────────────────
    print("\n⏳ Step 1: Extracting symptoms from text...")
    bart_result = symptom_extractor(
        user_input,
        candidate_labels=ALL_SYMPTOMS,
        multi_label=True,
    )
    extracted = [
        label for label, score
        in zip(bart_result["labels"], bart_result["scores"])
        if score > 0.15
    ][:5]
    print(f"   ✅ Extracted: {extracted}")

    # ── STEP 2: Severity Score from Dataset ──────────────
    print("\n⏳ Step 2: Calculating severity from dataset...")
    severity_score = calculate_severity_score(extracted)
    if severity_score >= 60:
        urgency = "HIGH"
        urgency_color = "🔴"
    elif severity_score >= 35:
        urgency = "MODERATE"
        urgency_color = "🟡"
    else:
        urgency = "LOW"
        urgency_color = "🟢"
    print(f"   ✅ Severity Score: {severity_score}/100 → {urgency_color} {urgency}")

    # ── STEP 3: Disease Matching ──────────────────────────
    print("\n⏳ Step 3: Matching diseases from dataset...")
    matched_diseases = match_disease_from_symptoms(extracted)
    print(f"   ✅ Top match: {matched_diseases[0][0] if matched_diseases else 'None'}")

    # ── STEP 4: Red Flag Detection ────────────────────────
    print("\n⏳ Step 4: Checking red flags...")
    red_flags = [
        (s, RED_FLAG_MAP[s])
        for s in extracted
        if s in RED_FLAG_MAP
    ]
    maternal_alerts = [
        (s, MATERNAL_WARNINGS[s])
        for s in extracted
        if is_pregnant and s in MATERNAL_WARNINGS
    ]
