# ── 1. Maternal Risk Predictor

def predict_maternal_risk(age, systolic_bp, diastolic_bp,
                           blood_glucose, body_temp, heart_rate):
    """
    Nearest-neighbor search on real Kaggle maternal dataset.
    Returns risk level + confidence.
    """
    user_vector = np.array([
        age, systolic_bp, diastolic_bp,
        blood_glucose, body_temp, heart_rate
    ], dtype=float)

    features = maternal_df[['age','systolicbp','diastolicbp',
                             'bs','bodytemp','heartrate']].values.astype(float)

    # Normalize
    col_max     = features.max(axis=0) + 1e-8
    norm_feat   = features / col_max
    norm_user   = user_vector / col_max

    # Find 10 nearest neighbors
    distances   = np.linalg.norm(norm_feat - norm_user, axis=1)
    top_k_idx   = distances.argsort()[:10]
    top_k_risks = maternal_df.iloc[top_k_idx]['risklevel'].values

    # Majority vote
    unique, counts  = np.unique(top_k_risks, return_counts=True)
    predicted_risk  = unique[counts.argmax()]
    confidence      = counts.max() / 10

    return predicted_risk, confidence


# ── 2. Disease Matcher

def match_disease_from_symptoms(user_symptoms):
    """
    Searches disease-symptom dataset for matching conditions.
    Returns top 3 with match scores.
    """
    matched = []

    for _, row in disease_symptom_df.iterrows():
        disease   = row.iloc[0]
        row_text  = " ".join(
            str(v).lower().replace("_"," ")
            for v in row.values
        )
        score = sum(
            1 for s in user_symptoms
            if s.lower().replace("_"," ") in row_text
        )
        if score > 0:
            matched.append((disease, score))

    matched.sort(key=lambda x: x[1], reverse=True)
    return matched[:3]


# ── 3. Severity Score Calculator

def calculate_severity_score(user_symptoms):
    """
    Uses real Symptom-severity.csv weights to
    calculate total severity score (0-100).
    """
    total = 0
    max_possible = 7 * len(user_symptoms)  # max weight is 7

    for symptom in user_symptoms:
        clean = symptom.lower().replace(" ","_")
        weight = symptom_severity_map.get(clean, 3)  # default weight 3
        total += weight

    score = min((total / max(max_possible, 1)) * 100, 100)
    return round(score, 1)


# ── 4. Precaution Fetcher

def get_precautions(disease_name):
    """
    Fetches real precautions from dataset for a disease.
    """
    match = precaution_df[
        precaution_df.iloc[:,0].str.lower() == disease_name.lower()
    ]
    if match.empty:
        return []

    precautions = []
    for col in match.columns[1:]:
        val = str(match.iloc[0][col]).strip()
        if val and val.lower() not in ["nan", "none", ""]:
            precautions.append(val)
    return precautions


